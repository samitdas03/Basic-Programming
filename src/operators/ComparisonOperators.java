package operators;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 7;
		
		boolean c = a > b;
		boolean d = a < b;
		boolean k = a != b;
		
		
		System.out.println(c);
		System.out.println(d);
		System.out.println(k);
		
		int e = 5;
		int f = 5;
				
		boolean g = e == f;
		boolean i = e >= f;
		boolean j = e <= f;
		
		System.out.println(g + " " + i + " " + j );
		System.out.println(e == f);
		
	}

}
